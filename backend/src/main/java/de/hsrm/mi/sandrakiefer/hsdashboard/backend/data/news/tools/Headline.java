package de.hsrm.mi.sandrakiefer.hsdashboard.backend.data.news.tools;

/**
 * Helper class headline for news class for storing the given information and export it as json.
 */
public class Headline {

    private String title;

    private String link;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
    
}
